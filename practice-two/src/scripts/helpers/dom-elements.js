/**
 * Get an element by ID using `getElementById`.
 * @param {string} id - The ID of the element to retrieve.
 * @returns {Element} - The element retrieved using the provided ID.
 */
export const getElementById = (id) => document.getElementById(id);

/**
 * Get an element by selector using `querySelector`.
 * @param {string} id - Selector CSS to choose the element.
 * @returns {Element} - The element retrieved using the provided selector.
 */
export const querySelector = (id) => document.querySelector(id);
